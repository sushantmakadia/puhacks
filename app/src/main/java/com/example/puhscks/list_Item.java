package com.example.puhscks;

public class list_Item {
    private String h;
    private String d;
    private  String q;

    public list_Item(String h,String d,String q){
        this.h=h;
        this.d=d;
        this.q=q;
    }

    public String getH() {
        return h;
    }

    public String getD() {
        return d;
    }

    public String getQ() {
        return q;
    }
}
